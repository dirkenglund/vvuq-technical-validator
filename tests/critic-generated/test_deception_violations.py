class TestNoDuplicateContent:
    """Verify plugin has genuine, non-duplicated content."""

    def test_commands_are_distinct(self):
        """Each command should have unique functionality."""
        commands_dir = PLUGIN_ROOT / "commands"
        if not commands_dir.exists():
            pytest.skip("No commands directory")

        contents = {}
        for cmd_file in commands_dir.glob("*.md"):
            content = cmd_file.read_text()
            # Extract description from frontmatter
            if "description:" in content:
                desc_line = [l for l in content.split("\n") if "description:" in l][0]
                contents[cmd_file.name] = desc_line

        # All descriptions should be unique
        descriptions = list(contents.values())
        assert len(descriptions) == len(set(descriptions)), \
            "Commands have duplicate descriptions"

    def test_benchmark_ids_unique(self):
        """All benchmark test case IDs must be unique."""
        for json_file in (PLUGIN_ROOT / "benchmarks" / "test_cases").glob("*.json"):
            data = json.loads(json_file.read_text())
            ids = [case["id"] for case in data["test_cases"]]
            assert len(ids) == len(set(ids)), \
                f"{json_file.name} has duplicate test case IDs"


class TestBenchmarkIntegrity:
    """Verify benchmark results are genuine."""

    def test_benchmark_results_have_metrics(self):
        """Benchmark results must include standard metrics."""
        results_dir = PLUGIN_ROOT / "benchmarks" / "results"
        if not results_dir.exists():
            pytest.skip("No results directory")

        for result_file in results_dir.glob("*.json"):
            data = json.loads(result_file.read_text())
            # Must have some form of metrics
            assert any(key in str(data).lower() for key in
                      ["f1", "precision", "recall", "accuracy"]), \
                f"{result_file.name} missing evaluation metrics"

    def test_no_hardcoded_perfect_scores(self):
        """Results should not have suspicious perfect scores everywhere."""
        results_dir = PLUGIN_ROOT / "benchmarks" / "results"
        if not results_dir.exists():
            pytest.skip("No results directory")

        for result_file in results_dir.glob("*.json"):
            content = result_file.read_text()
            # If all scores are exactly 1.0, that's suspicious
            # Allow some perfect scores, but not ALL
            data = json.loads(content)
            if isinstance(data, dict) and "models" in data:
                models = data["models"]
                # Handle both list and dict formats
                if isinstance(models, list):
                    all_perfect = all(
                        model.get("f1", 0) == 1.0
                        for model in models
                    )
                else:
                    all_perfect = all(
                        model.get("f1", 0) == 1.0
                        for model in models.values()
                    )
                assert not all_perfect, \
                    f"{result_file.name} has suspiciously perfect scores"


